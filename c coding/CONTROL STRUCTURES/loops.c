#include<stdio.h>

int main()
{
    int i = 0;
    while (i < 20)
    {
        printf("%d ", i);
        i++;
    }

    printf("\n");

    int x = 0;
    do
    {
        printf("%d ", x);
        x++;
    }
    while(x < 10);

    printf("\n");

    // FOR
    for (int r = 15 ; r <= 30 ; r++)
    {
        printf("%d ", r);
    }

     






    return 0;
}